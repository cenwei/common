package org.bclove.common.utils;

public class TimeUtil {
	public final static int SECOND = 1000;
	public final static int MINUTES = SECOND * 60;
	public final static int HOUR = MINUTES * 60;
	public final static int DAY = HOUR * 24;
	public final static int WEEK = DAY * 7;
	public final static int MONTH = DAY * 30;

}
